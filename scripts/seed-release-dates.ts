async function main() {
  console.log('--- 開始更新歌曲發行日期 ---');
  let updateCount = 0;

  const entries = Object.entries(releaseDateMap);
  const total = entries.filter(([_, date]) => date.trim() !== '').length;
  console.log(`共有 ${total} 首歌曲填寫了發行日期，開始寫入資料庫...`);

  for (const [title, releaseDate] of entries) {
    if (!releaseDate || releaseDate.trim() === '') {
      continue; // 略過未填寫日期的歌曲
    }

    try {
      const result = await prisma.song.updateMany({
        where: {
          title: title,
        },
        data: {
          releaseDate: releaseDate.trim(),
        },
      });

      if (result.count > 0) {
        console.log(`[更新成功] 歌曲: 「${title}」 -> 發行日期: ${releaseDate} (${result.count} 首)`);
        updateCount += result.count;
      } else {
        console.warn(`[找不到歌曲] 資料庫中找不到曲名為 「${title}」 的歌曲。`);
      }
    } catch (error: any) {
      console.error(`[更新失敗] 歌曲: 「${title}」 寫入錯誤: ${error.message}`);
    }
  }

  console.log(`\n--- 發行日期更新完畢！共更新了 ${updateCount} 筆歌曲記錄 --- `);
}

main()
  .catch((e) => {
    console.error('執行 Seed 腳本時發生錯誤:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
